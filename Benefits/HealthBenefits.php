            <h2 class="hb_header">Choice Plus PPO</h2>
            <p>The <strong>Choice Plus PPO</strong> option gives you the flexibility you’ve already come to know with a PPO by allowing direct access to the network physicians or specialists without having to obtain a referral. The Choice Plus PPO option also enables you to seek services from non-network providers and still receive plan benefits. However, keep in mind that plan benefits are greater when you use network providers. In addition, you will be responsible for paying the remainder of expenses after the plan has paid its portion.</p>
            <a class="hb_a_list" href="/benefits/PDF/SPD_UHC_Choice_Plus_PPO.pdf" target="_blank">PPO Summary Plan Description</a><br/><br/>

            <h2 class="hb_header">Indemnity Plan</h2>
            <p>The <strong>Indemnity Plan</strong> option provides maximum freedom for dealing with any health care situation. This incredibly flexible program lets you make your own health care decisions, including which doctors or specialists to visit. Benefits are provided for covered health services received from any physician or other licensed medical provider. When you receive covered health services from the national network of physicians, hospitals and other health care providers, you benefit from the contracted rates and fees that have been negotiated. <em>The Indemnity plan is only available in areas where there is no EPO or PPO Network available.</em></p>

            <h2 class="hb_header">Choice EPO</h2>
            <p>The <strong>Choice EPO</strong> option gives you flexibility and control over your health care. Similar to an HMO, the Choice EPO gives you freedom to see any doctor in the UnitedHealthcare network, including Specialists, without getting a referral! Plus, when you visit network doctors and hospitals, there are no claim forms to complete or bills to worry about. Another advantage to the Choice EPO is that you can move to a different location without an interruption of service. With the open access feature of the Choice EPO option, you have access to any network physician throughout the country.</p>

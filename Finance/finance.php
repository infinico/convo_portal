    <h1 class="headerPages">Convo Travel &amp; Reimbursement Policy </h1>
    
    <h2>Overview</h2>
    <p>It is the policy of Convo Communications to reimburse staff for reasonable and necessary expenses incurred during required business activity and travel. Reimbursement is allowed only when reimbursement has not been, and will not be, received from other sources.</p>

<h2>Processing</h2>
<p>Supervisors must authorize all travel. Travelers should verify with Supervisors that planned travel is eligible for reimbursement before making travel arrangements.</p>
<p>The Finance Department will take care of all travel arrangements. </p>            
<p>Reimbursement rules apply to both company credit cards and personal funds.</p>

<h2>Travel Advances</h2>
<p>Per Diem cash advances are authorized for all travel given two weeks’ notice. </p>
<p>Additional advances in lieu of specific situations that might cause undue financial hardship for business travelers are dealt with on a case-by-case basis. These situations are limited to staff traveling on behalf of Convo. The traveler should request to receive such an advance from the Finance Department with at least three weeks’ notice.</p>
<p>Travel advances must be a minimum of $100.</p>

<h2>Booking of Travel</h2>
<p>All travel is to be booked by the Finance Department by contacting <a href= "mailto:purchasing@convorelay.com">purchasing@convorelay.com</a> to request reservations at least 21 days prior to traveling.</p>
<p>Air travel is Economy/Coach, and appropriate class rentals must be purchased. All upgrades must have prior approval.</p>
<p>Lodging should be obtained at reasonable single occupancy or standard business room rates.</p>
<p>Travelers must request approval if requesting vacation built into business travel.  If approved, any cost variance in airfare, car rental, or lodging must be clearly identified on the <a href="Travel_Request_Form.xlsx">Travel Request form</a> with the pre-approved allocation method.</p>

<h2>Conference and Registration Fees</h2>
<p>Approved conference and registration fees can be prepaid with a credit card or paid through the Finance Department. Business-related banquets or meals that are considered part of the conference can be paid with the registration fees.</p>

<h2>Post-Travel</h2>
<p>Travelers should submit a <a href="Reimbursement_Form.xlsx">Travel Reimbursement form</a> with supporting documentation, including itemized receipts, within five business days of the completion of a trip. Documentation should reflect reasonable and actual expenses occurred while transacting business on behalf of Convo. </p>
<p>Expenses associated with the travel must be reconciled and substantiated within the Travel Reimbursement form. The Supervisor must approve said reimbursement before submitting to the Finance Department. </p>
<p>When approved to book travel directly, reimbursement must include a fully itemized receipt of travel dossier or e-ticket receipt/statement. The receipt must show the method of payment and indicate that payment was made.</p>
<p>Additional costs for travel, lodging, meals, or other travel expenses for spouses or other family members will not be reimbursed.</p>
<p>Convo reserves the right to withhold reimbursement if expenses are deemed to be inappropriate or extravagant. </p>
<p>The traveler must repay Convo for any advances in excess of the approved reimbursable expenses.</p>
<p>Reimbursements will be mailed within 10 business days of final departmental approval.</p>
    
<h2>Personal Automobiles</h2>
<p>A valid U.S. Driver’s License, current vehicle registration, and personal automobile insurance are required for employees to use their personal vehicles for reimbursable employment purposes. Drivers should be aware of the extent of coverage (if any) provided by his or her automobile insurance company for travel that is business or not personal in nature. Convo may choose at its own discretion to get additional insurance to supplement the individual’s insurance.</p>
<p>Reimbursement for use of a personal automobile is based on the current Convo mileage rate of $0.56 per mile. Travelers are required to submit online maps (MapQuest, Google) in PDF format with their Travel Reimbursement form as documentation with mileage totals showing exact locations.</p>
<p>Travelers may opt to request reimbursement for gasoline in lieu of the Convo mileage rate. In these instances, original itemized receipts are required. </p> 

<h2>Automobile Rentals</h2>
<p>Convo authorizes reimbursement for the most economic vehicle available. In certain circumstances, larger vehicles may be rented with Supervisor approval. The rental agreement must clearly show the date and the points of departure/arrival, as well as the total cost. Drivers must adhere to the rental requirements, and restrictions must be followed. Original receipts are required.</p>
<p>All Rentals must have collision damage waiver (CDW) and loss damage waiver (LDW) coverage purchased. Convo will reimburse the cost of CDW and LDW coverage if not paid by the Finance Department; all other insurance reimbursements will be denied. Do not purchase insurance locally unless approved in advance. </p>
<p>Travelers are required to fill the gas tank before returning the vehicle to the rental agency to avoid service fees and more expensive fuel rates. Employees are liable if extra charges are incurred due to negligence.</p> 
<p>Employees are not allowed to use Uber Black or any top-tier automobile rental methods at any given time.</p>

<h2>Meals (Per Diem)</h2>
<p>Per Diem allowances are reimbursable for overnight travel and are based on departure and return times over the entire 24-hour day as follows:  </p>
<p><b><i>Breakfast $8</i></b>: Departing by 7am or arriving by 10am during travel</p>
<p><b><i>Lunch $12</i></b>: Departing by 11am or arriving by 2pm during travel</p>
<p><b><i>Dinner $20</i></b>: Departing by 5pm or arriving after 8pm during travel</p>
<p>A full day’s Per Diem will be $40 for all three meals. All other days will be prorated accordingly with the above schedule. Any company-sponsored meals must have the Per Diem deducted from the Travel Reimbursement form.  If any other free meals are provided such as served on the plane, included with a conference, or part of any company function/meal, said Per Diem should not be reimbursed.</p>
<p>Travelers are required to put in times of departure/arrival on the Travel Reimbursement form.</p>
<p>Incidentals such as coffee and drinks outside the Per Diem will not be covered. No alcohol is allowed.</p>
<p>Receipts are not required for Per Diem allowances. </p>

<h2>Business Meals</h2>
<p>Travelers must receive Supervisor approval for business meals outside of the Per Diem in advance of the expense. Original itemized receipts are required. No alcohol is allowed unless with prior approval. </p>

<h2>Business Expenses</h2>
<p>Business expenses, including faxes, photocopies, internet charges, data ports, and business telephone calls incurred while on travel status may be reimbursed. Original itemized receipts are required.  Travelers must use company credit cards and not personal credit cards. Personal meals are not reimbursable. Supervisor approval in advance of purchase is required and failure to get Supervisor approval in advance of purchase can result in the expense being disallowed.</p>

<h2>Parking</h2>
<p>Original receipts are required for all parking fees.</p>
<p>Travelers are expected to obtain the lowest cost available parking, including long-term airport parking and off-site parking.</p>

<h2>Tickets/Tolls</h2>
<p>Original receipts are required for toll reimbursements for automobile, taxi, bus, subway, metro, ferry, or any other modes of transportation.</p>

<h2>Non-Reimbursable Travel Expenses</h2>
<p>The following items will not be reimbursed: </p>
<p> - Airline club memberships</p>
<p> - Airline upgrades without prior approval</p>
<p> - Childcare, babysitting, housesitting, or pet-sitting/kennel charges</p>
<p> - Commuting reimbursements for between home and primary work locations</p>
<p> - Cancellation fees incurred by traveler’s failure to cancel in a timely fashion</p>
<p> - Evening or formal wear expenses</p>
<p> - Haircuts and personal grooming</p>
<p> - Laundry and dry cleaning</p>
<p> - Passports, vaccinations and visas when not required as a specific and necessary condition of the travel assignment</p>
<p> - Personal entertainment expenses, including in-flight movies, headsets, health club facilities, hotel pay-per-view movies, in-theater movies, social activities and related incidental costs</p>
<p> - Travel accident insurance premiums or purchase of additional travel insurance without approval</p>
<p> - Other expenses not directly related to the business travel</p>

<p><em>If you have any additional questions, please don’t hesitate to ask your Supervisor or contact a member of the Finance Department.</em></p>

